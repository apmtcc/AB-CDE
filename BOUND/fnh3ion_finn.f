C
C       H3O2NNPOT, R(O1H2,O1H3,...)/ANGSTROM, V(NPES)/EV
C	
        SUBROUTINE NNPOT(R,VOUT)
        USE NNDATA
        IMPLICIT NONE
        REAL*8,INTENT(IN)  :: R(DIN)
        REAL*8 V(NPES)
        REAL*8 VOUT,RMSE
        INTEGER :: IP,I,J,K,jjj
        REAL*8 :: VFIT,TEST

        DO IP=1,NPES
C
C	INPUT LAYER
C
        DO I=1,DIN
        RLI(I,1,IP)=2.D0*(R(I)-XRANGE(1,I,IP))/(XRANGE(2,I,IP)
     &             -XRANGE(1,I,IP))-1.D0
        ENDDO
C
C	layer 1-3
C
        DO I=1,NLAYS
        RLI(1:NEU(I+1),I+1,IP)=B(1:NEU(I+1),I,IP)
        DO J=1,NEU(I+1)
        IF (I==NLAYS)THEN
        DO K=1,NEU(I)
        RLI(J,I+1,IP)=RLI(J,I+1,IP)+W(K,J,I,IP)
     &               *RLI(K,I,IP)
        ENDDO
        ELSE
        DO K=1,NEU(I)
        RLI(J,I+1,IP)=RLI(J,I+1,IP)+W(K,J,I,IP)
     &               *RLI(K,I,IP)
        ENDDO
        TEST=-2.D0*RLI(J,I+1,IP)
        IF(TEST<-100.D0) THEN
        RLI(J,I+1,IP)=1.D0
        ELSE IF(TEST>-100.D0 .AND. TEST<100.D0) THEN
        RLI(J,I+1,IP)=2.D0/(1.D0+EXP(TEST ))-1.D0
        ELSE
        RLI(J,I+1,IP)=-1.D0
        ENDIF
        ENDIF
        ENDDO
        ENDDO
        VFIT=RLI(1,NLAYS+1,IP)
        V(IP)=0.5D0*(VFIT+1.D0)*(YRANGE(2,IP)-YRANGE(1,IP))
     &       +YRANGE(1,IP)
        ENDDO

        VOUT=0.D0

        DO I=1,NPES
           VOUT=VOUT+V(I)            ! V average, uint in eV
        ENDDO
        VOUT=VOUT/(NPES*1.D0)
C...rmse
!        RMSE=0.D0
!        DO jjj=1,NPES
!           RMSE= RMSE+(V(jjj)-VOUT)**2
!        END DO
!        RMSE=RMSE/(NPES*1.D0)
!        RMSE=DSQRT(RMSE)*1000d0

        RETURN
        END

